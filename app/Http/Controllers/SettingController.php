     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Setting  $setting
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        // dd($request->all());
        $request->validate([
            'name' => 'required',
            'value' => 'required',
        ]);

        $setting = Setting::find($id);
        $setting->update($request->all());

        if($setting){
            return redirect()->route('setting-perusahaan')->with(['success' => 'Data'.$request->input('name').'berhasil disimpan']);
        }else{
            return redirect()->route('setting-perusahaan')->with(['danger' => 'Data Tidak Terekam!']);
        }
    }

